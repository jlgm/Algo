class SafeBetting {
public:
	int minRounds(int, int, int);
};

int SafeBetting::minRounds(int a, int b, int c) {
	int ans = 0;
	int cur = b;
	while(cur < c) {
		int dif = cur-a;
		cur -= dif;
		cur += 2*dif;
		ans++;
	}
	return ans;
}


double test0() {
	int p0 = 15;
	int p1 = 20;
	int p2 = 48;
	SafeBetting * obj = new SafeBetting();
	clock_t start = clock();
	int my_answer = obj->minRounds(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 3;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
