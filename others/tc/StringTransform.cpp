class StringTransform {
public:
	string isPossible(string, string);
};

string StringTransform::isPossible(string s, string t) {
	
	for (int i = t.size()-1; i >= 0; i--) {
		bool fnd = false;
		if (s[i] == t[i]) continue;
		for (int j = i-1; j >= 0; j--) {
			if (s[j] == t[i]) { fnd = true; break; }
		}
		if (!fnd) return "Impossible";
	}
	return "Possible";	
}


